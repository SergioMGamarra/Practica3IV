                    <p id="tituloNoticia" onmouseover="abrir_dialog()">Los patrimonios aflorados tras la amnistía fiscal sólo permitirán recaudar 250 millones este año</p>
                    <img src="imagenes/montoro.jpg"/>
                    <p id="descripcionImagen">Ministro de Hacienda y Administración Públicas, Cristóbal Montoro</p>
                    <p class="cuerpoNoticia">
                        Los patrimonios aflorados tras el proceso de amnistía fiscal permitirán recaudar 250 millones de euros este año, a pesar de que el importe aflorado ascendió a 40.000 millones de euros, según calcula el Gobierno en el Programa Nacional de Reformas 2013, que ha enviado a Bruselas. 

                    </p>
                    <p class="cuerpoNoticia">
                        El proceso de regularización extraordinario que aprobó el Ejecutivo el pasado año, conocido como 'amnistía fiscal', permitió recaudar 1.200 millones de euros, la mitad de lo previsto inicialmente por el departamento de Cristóbal Montoro, que no obstante indicó que las bases afloradas ascendían a 40.000 millones de euros. Ahora, estima que de este importe aflorado las arcas públicas ingresarán 250 millones en 2013.

                    </p>

                    <p class="cuerpoNoticia">
                        Fuentes de Hacienda precisaron que la previsión es "prudente" y que la cifra podría ser superior al acabar el año. Además, aseguran que los 250 millones no tienen en cuenta las declaraciones extemporáneas que se están presentando estos meses como de la Declaración Tributaria Especial (DTE).


                    </p>

                    <p class="cuerpoNoticia">  
                        Además, el plan nacional de reformas contempla que la recaudación por las medidas contra el fraude fiscal se incrementará en 1.000 millones de euros tanto en 2013 como en 2014, tras alcanzar los 11.517 millones de euros en 2012, un 10% más que un año antes.

                    </p>
                    <p class="cuerpoNoticia">
                        Más recaudación por la subida del IRPF el Gobierno también cuantifica en su plan nacional de reformas que recaudará 2.022 millones de euros gracias a la subida del IRPF, que se extenderá también a 2014, un año más de lo inicialmente previsto.
                    </p>
                    <p class="cuerpoNoticia">
                        La eliminación de la deducción por compra habitual, que entró en vigor el 1 de enero de este año, supondrá un ahorro de 90 millones de euros cada año. Además, por el gravamen del 20% en los premios de loterías espera ingresar 825 millones este año.
                    </p>
