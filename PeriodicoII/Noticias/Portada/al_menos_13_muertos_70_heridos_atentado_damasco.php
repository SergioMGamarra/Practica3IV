                    <p class="cuerpoNoticia">
                        Al menos 13 personas murieron y 70 resultaron heridas hoy por un atentado con coche bomba en el centro de Damasco, informó la agencia de noticias oficial siria Sana.
                    </p>
                    <p class="cuerpoNoticia">
                        Por su parte, Rami Abderrahman, presidente del opositor Observatorio Sirio de Derechos Humanos con sede en Londres, precisó que la explosión ocurrió en la puerta trasera de la antigua sede del Ministerio del Interior.
                    </p>

                    <p class="cuerpoNoticia">
                        Las primeras informaciones afirmaban que había ocurrido en el actual edificio de ese departamento, según informó la agencia de noticias EFE.
                    </p>

                    <p class="cuerpoNoticia">  
                        El ministerio funcionó allí hasta hace dos meses, cuando fue trasladado tras otro ataque rebelde.
                    </p>
                    <p class="cuerpoNoticia">
                        En imágenes mostradas por el canal de noticias Al Ajbariya se pudo observar varios vehículos calcinados y equipos de emergencia rescatando a las víctimas.
                    </p>
                    <p class="cuerpoNoticia">
                        El ataque ocurre un día después de que un coche bomba estallara frente al convoy en el que se trasladaba el primer ministro, Wael al Halqui, en el centro de la capital.
                    </p>
