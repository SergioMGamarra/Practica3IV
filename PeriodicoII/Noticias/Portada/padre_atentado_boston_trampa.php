                    <p id="tituloNoticia" onmouseover="abrir_dialog()"> La madre de los sospechosos de atentar en Boston:"Son buenos chicos. Mis hijos son inocentes"</p>
                    <img src="imagenes/padreBoston.jpg"/>
                    <p id="descripcionImagen">Anzor Tsarnaev, el padre de los sospeschosos</p>
                    <p class="cuerpoNoticia">
                        La madre de Dzhokhar y Tamerlan Tsarnaev, los sospechosos de haber cometido el atentado de Boston, asegura que "son buenos chicos" e "inocentes" respondiendo así a las acusaciones de terrorismo que se ciernen sobre sus hijos, uno de ellos ya fallecido, mientras que el otro se encuentra en estado grave.
                    </p>
                    <p class="cuerpoNoticia">
                        Zubeidat Tsarnaeva, según informa DT, asegura que Dzhokhar es un gran estudiante, querido por sus compañeros y profesores, mientras que Tamerlan, además de destacar en los estudios, era un gran atleta, según su madre.
                    </p>

                    <p class="cuerpoNoticia">
                        "Él (Dzhokhar) me habría dicho que su hermano era un líder extremista, me habría dicho cualquier cosa que supiese", sostiene Tsarnaeva, que insiste en que "nada es verdad".

                    </p>

                    <p class="cuerpoNoticia">  
                        Sobre si sus hijos podrían haberle ocultado sus planes, la madre de los sospechosos afirma que es "imposible, mi hijo nunca guardaría un secreto", en referencia al hermano pequeño.

                    </p>
                    <p class="cuerpoNoticia">
                        Zubeidat, que mantiene que Dzhokhar, ingresado en el hospital en estado grave y bajo custodia policial, no ha cometido el atentado, reveló que, de poder hablar con él, le diría que "salve su vida y dijese la verdad, que no había hecho nada", ya que "todo esto es una trampa".
                    </p>
                    <p class="cuerpoNoticia">
                        Esta tesis de la trampa la comparte el padre de los jóvenes chechenos, Anzor Tsarnaev, que está "convencido" de la inocencia de sus hijos, pero que ahora teme por la vida del pequeño, ya que "la justicia debe investigar quién es inocente y quién culpable".
                    </p>
