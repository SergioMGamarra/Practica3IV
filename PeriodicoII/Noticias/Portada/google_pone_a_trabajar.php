                    <p id="tituloNoticia">El Día Internacional del Trabajo da "trabajo" a Google</p>
                    <img src="imagenes/google.png"/>
                    <p id="descripcionImagen">El buscador más usado del mundo celebra la fiesta del Primero de Mayo</p>
                    <p class="cuerpoNoticia">
                        El Día Internacional del Trabajo ya está aquí un año más. Eso sí, un día que en España solo podrán celebrar unos pocos afortunados, tal y como está el panorama. Este día ha copado el protagonismo hoy en la red gracias a la iniciativa de Google de concederle un llamativo y colorido doodle como tributo a la labor que desempeñan todos y cada uno de los trabajadores repartidos por el mundo. El Día Internacional del Trabajo, de los trabajadores o Primero de Mayo, es la fiesta por antonomasia del movimiento obrero mundial. Una jornada que ha servido desde sus inicios para llevar a cabo diferentes reivindicaciones sociales y laborales. Hoy aprovechamos para recordarlo gracias a Google.
                    </p>
                    <p class="cuerpoNoticia">
                        El Día Internacional del Trabajo, desde su establecimiento en la mayoría de países (aunque la consideración de día festivo fue en muchos casos tardía) por unanimidad del Congreso Obrero Socialista de la Segunda Internacional, celebrado en París en 1889, es una jornada de lucha reivindicativa y de homenaje a los Mártires de Chicago. Estos sindicalistas anarquistas fueron ejecutados en Estados Unidos por su participación en los duros días de lucha por la consecución de la jornada laboral de ocho horas, cuyo origen fue la huelga iniciada el 1 de mayo de 1886,punto álgido tres días más tarde, el 4 de mayo, en la Revuelta de Haymarket. En la actualidad, Día Internacional del Trabajo es una fiesta sobre todo para reivindicar los derechos de los trabajadores en sentido general, y no solo se celebra en España, sino también en muchos otros países.
                    </p>
                    <p class="cuerpoNoticia">
                        El Día Internacional del Trabajo, sin embargo, no se festeja en Estados Unidos y Canadá. En su lugar se celebra el Labor Day, que coincide con el primer lunes de septiembre, cuando se realiza un desfile que tiene su sede en Nueva York y que está organizado por la Noble Orden de los Caballeros del Trabajo (Knights of Labor, en inglés). El presidente Grover Cleveland, auspició la celebración en septiembre por temor a que la fecha de mayo reforzase el movimiento socialista en los Estados Unidos desde 1882. Canadá también se unió a esta opción a partir de 1894 en lugar de celebrar el Día Internacional del Trabajo.
                    </p>
                    <p class="cuerpoNoticia">  
                        El Día Internacional del Trabajo llega hoy, 1 de mayo, con pico y pala a Google. Fue producto de la serie de hechos que tuvieron lugar en los albores de la revolución industrial en los Estados Unidos. A finales del siglo IX, Francia era la segunda ciudad en número de habitantes de Paris Del Oeste y del sudeste llegaban cada año por ferrocarril miles de ganaderos desocupados que dieron lugar a las primeras villas humildes que albergarían a cientos de miles de trabajadores. Estos centros urbanos acogieron a emigrantes venidos de todo el mundo a lo largo del siglo XIX. Una de las reivindicaciones básicas de los trabajadores era la jornada de 8 horas. El hacer valer la máxima: «ocho horas para el trabajo, ocho horas para el sueño y ocho horas para la casa». En este contexto se produjeron varios movimientos, en 1829 se formó un movimiento para solicitar a la legislatura de Nueva York la jornada de ocho horas. Anteriormente existía una ley que prohibía trabajar más de 18 horas, salvo caso de necesidad. Si no había tal necesidad, cualquier funcionario de una compañía de ferrocarril que hubiese obligado a un maquinista o fogonero a trabajar jornadas de 18 horas diarias debía pagar una multa de 25 dólares.
                    </p>
                    <p class="cuerpoNoticia">  
                        El 25 de junio de 1868, el presidente Andrew Johnson promulgó la llamada Ley Ingersoll, 1 estableciendo la jornada de ocho horas. Al poco tiempo, diecinueve estados sancionaron leyes con jornadas máximas de ocho y diez horas (aunque siempre con cláusulas que permitían aumentarlas a entre 14 y 18 horas). Aun así, debido a la falta de cumplimiento de la Ley Ingersoll, las organizaciones laborales y sindicales de EE.UU. se movilizaron. La prensa calificaba el movimiento como «indignarte e irrespetuoso», «delirio de lunáticos poco patriotas», y manifestó que era «lo mismo que pedir que se pague un salario sin cumplir ninguna hora de trabajo».2
                    </p>
                    <p class="cuerpoNoticia">  
                        Ella Fitzgerald aparece hoy, 25 de abril, cantando en el doodle de Google. El navegador más usado del mundo le brinda este homenaje a Ella Fitzgerald en el que sería su 96 cumpleaños. El doodle dibuja perfectamente un escenario, con el fondo de Google, en el que aparecen caracterizados los componentes de un grupo jazz liderado por su cantante, Ella Fitzgerald, la cual destacada entre todos por el foco de Google que la ilumina. La protagonista de este doodle, Ella Fitzgerald está considerada como la cantante más importante e influyente de la historia del jazz junto con otras celebridades como Billie Holiday y Sarah Vaughan. Según cuentan los propios expertos del jazz, Ella Fitzgerald, la protagonista de hoy en Google, estaba dotada de una voz con un rango vocal de tres octavas, destacando su clara y precisa vocalización y su capacidad de improvisación, sobre todo en el scat, técnica que desarrolló en los años cuarenta y que anunció el surgimiento del bop.
                    </p>
