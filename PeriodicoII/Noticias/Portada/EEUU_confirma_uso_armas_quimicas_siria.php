                    <p id="tituloNoticia">EE.UU. confirma el uso de armas químicas en Siria, pero no sabe quién las llegó a emplear</p>
                    <img src="imagenes/obama.jpg"/>
                    <p id="descripcionImagen">Obama en su última aparición pública</p>
                    <p class="cuerpoNoticia">
                        El presidente estadounidense, Barack Obama, ha anunciado este martes que Estados Unidos tiene pruebas de que armas químicas han sido usadas en territorio sirio, pero que desconoce todavía quién las empleó y en qué circunstancias. Obama defendió que es necesario conocer el contexto en el que se produjeron los ataques antes de tomar «medidas adicionales» contra el régimen de Assad.
                    </p>
                    <p class="cuerpoNoticia">
                        «Sabemos que se usaron armas químicas, pero no sabemos cómo se usaron, dónde se usaron y quiénes las usaron», ha advertido el presidente. «Cuando tomo decisiones de seguridad nacional [...], tengo que estar seguro de que tenemos todos los datos», apostilló.
                    </p>
                    <p class="cuerpoNoticia">
                        El presidente ha insistido en la idea de que el uso de ese tipo de armamento supondría una «línea roja», como ya había indicado con anterioridad. Sin embargo, ha dicho que un «juicio precipitado» sobre este hecho podría «dificultar la movilización de la comunidad internacional», informa la agencia Reuters.
                    </p>
                    <p class="cuerpoNoticia">  
                        Afirmó que Washington está «muy involucrado» en la búsqueda de una solución e insistió en que su Gobierno utilizará «todos los recursos» a su alcance para aclarar el uso de armas químicas. «Tenemos que saber todo lo que ha ocurrido y está ocurriendo en Siria. Estamos preparados y comprometidos con dar una solución pacífica a este problema», comentó durante la rueda de prensa.
                    </p>
