                    <p id="tituloNoticia">Los sindicatos reclaman este Primero de Mayo un cambio radical en la política económica</p>
                    <img src="imagenes/sindicatos.jpg"/>
                    <p id="descripcionImagen">Manifestación día del trabajo en Madrid</p>
                    <p class="cuerpoNoticia">
                        La actual crisis económica, y en especial las últimas cifras que sitúan el paro en más de 6,2 millones de personas, marcan las movilizaciones y concentraciones convocadas por los principales sindicatos con motivo del Primero de mayo, Día Internacional del Trabajador. 'No tienen límite. Lucha por tus derechos', es el lema de CC OO y UGT para este miércoles en un contexto de recesión que evidencia el "rotundo fracaso" de las políticas de recortes "impuestas por las instituciones y los gobiernos europeos".
                    </p>
                    <p class="cuerpoNoticia">
                        En un manifiesto, ambos sindicatos demandan "el establecimiento de un Tratado sobre la Europa Social que apueste por un cambio del modelo sustentado en la igualdad laboral y salarial entre hombres y mujeres, y la eliminación de todas las formas de discriminación; conformando por objetivos racionales en materia de empleo, reducción de la tasa de pobreza, derecho al alojamiento y reducción del analfabetismo funcional, la solidaridad y ayuda al desarrollo. Asimismo, defienden que el BCE respalde la deuda pública de los países, la modificación de las políticas de austeridad y el saneamiento del sistema financiero.
                    </p>
                    <p class="cuerpoNoticia">
                        Los secretarios generales de CC OO, Ignacio Fernández Toxo, y de UGT, Cándido Méndez, califican de "fracaso absoluto" las políticas adoptadas por el Gobierno del Partido Popular por estar condenados a "mayor recesión económica, más paro y más desigualdad". Ambos encabezarán la manifestación de Madrid, que saldrá a las 12.00 horas de la Plaza de Neptuno y terminará en la Puerta del Sol.
                    </p>
                    <p class="cuerpoNoticia">  
                        La Confederación Europea de Sindicatos (CES) hizo un llamamiento a celebrar este día como una jornada de movilización en contra de la austeridad y en favor del crecimiento, el empleo y el progreso social. "En muchos países europeos, el Primero de Mayo será un día de movilización en contra de la austeridad y en favor del empleo y el progreso social. El mayor problema sigue siendo el desempleo masivo, que está aumentando y que se une a una mayor precariedad y brecha de desigualdad" social, señaló en un comunicado la secretaria general de la CES, Bernardette Ségol.
                    </p>
                    <p class="cuerpoNoticia">
                        Añadió que "la lucha contra el paro tiene que ser una prioridad de la Unión Europea (UE) y los sindicatos aprovecharán el Primero de Mayo para denunciar el fracaso de las políticas macroeconómicas, que amenazan más que nunca el proyecto y la democracia de Europa". En este sentido, recordó que en el Consejo Europeo de finales de junio se decidirá sobre una hoja de ruta para la dimensión social de la Unión Económica y Monetaria, la cual, agregó Ségol, "no es un lujo, sino que tiene que ser una parte integral de toda política".
                    </p>
                    <p class="cuerpoNoticia">
                        Según Ségol, "seguiremos movilizándonos con el claro mensaje de que la lucha contra el desempleo y en favor de la cohesión social tiene que ser una prioridad absoluta" para la UE.
                    </p>
