                    <p id="tituloNoticia" onmouseover="abrir_dialog()">El juez Ruz cita como imputados a nueve empresarios que aparecen en los papeles de Bárcenas</p>
                    <img src="imagenes/juezRuz.jpg"/>
                    <p id="descripcionImagen">El Juez Ruz a la salida del juzgado</p>
                    <p class="cuerpoNoticia">
                        El juez de la Audiencia Nacional Pablo Ruz ha citado como imputados, entre los próximos días 27 y 29 de mayo, a nueve supuestos donantes del Partido Popular (PP) que aparecen en la contabilidad B atribuida a su extesorero Luis Bárcenas, entre los que se encuentran el presidente de la constructora OHL, Juan Miguel Villar Mir, y el expresidente de Sacyr Vallehermoso, Luis del Rivero.
                    </p>
                    <p class="cuerpoNoticia">
                        En un auto notificado este martes en el marco de la pieza separada del 'caso Gürtel' en la que se investigan los 'papeles de Bárcenas', Ruz cita también a declarar como testigos a cargos 'populares' que admitieron haber cobrado dinero reflejado en los 'papeles de Bárcenas', entre los que se encuentra el presidente del Senado, Pío García Escudero, y el expresidente de Baleares Jaume Matas. 
                    </p>
                    <p class="cuerpoNoticia">
                        Los imputados son el presunto número dos de la red 'Gürtel', Pablo Crespo; y los empresarios Juan Manuel Villar Mir (OHL), Luis de Rivero (Sacyr Vallehermoso), José Luis Sánchez Domínguez (Grupo Sando), Manuel Contreras Caro (Azvy), Alfonso García Pozuelo (Construcciones Hispánica), Juan Manuel Fernández (Aldesa), José Mayor Oreja (Fomento de Construcciones  y Contratas) y Antonio Vilella (Construcciones Rubau.
                    </p>
                    <p class="cuerpoNoticia">  
                        El juez de la Audiencia Nacional Javier Gómez Bermúdez les citó a declarar el pasado 11 de marzo tras admitir a trámite una querella presentada por Izquierda Unida (IU) en la que estaban imputados Bárcenas y el también tesorero 'popular' Alvaro Lapuerta. 
                    </p>
                    <p class="cuerpoNoticia">
                        La Sección Segunda de la Sala de lo Penal anuló las declaraciones de los extesoreros el pasado 21 de marzo y Ruz aplazó el resto tras asumir la causa en detrimento de Gómez Bermúdez por orden de la Sección Tercera.
                    </p>
